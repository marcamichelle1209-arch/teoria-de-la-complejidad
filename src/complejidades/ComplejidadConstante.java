package complejidades;

public class ComplejidadConstante {

    /**
     * Complejidad: O(1)
     * Razón: El número de operaciones es fijo.
     * No depende del tamaño de los datos.
     */

    public void ejemplo() {

        System.out.println("Ejemplo O(1):");

        int x = 10;
        int y = 20;
        int suma = x + y;

        System.out.println("Resultado: " + suma);

    }
}